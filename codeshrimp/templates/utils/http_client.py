#!/usr/bin/env python3
"""
Purpose: 安全 HTTP 客户端工具 v2 — 为安全测试脚本提供统一的 HTTP 请求封装
         支持：请求重试、代理、速率限制、SSL 控制、请求/响应日志、
              请求历史回放、WAF 检测、批量请求、异步模式
Auth: 仅限授权安全测试使用
Dependencies: requests, urllib3, httpx (可选, 异步支持)
Usage:
    from http_client import SecureHttpClient, ClientConfig

    # 同步模式
    client = SecureHttpClient(proxy="http://127.0.0.1:8080", rate_limit=2.0)
    resp = client.get("https://target/api", params={"id": 1})

    # 异步模式
    async with SecureHttpClient.async_client() as client:
        resp = await client.aget("https://target/api")

    # 批量请求
    results = client.batch_get(["https://t1.com", "https://t2.com"], concurrency=10)

    # WAF 检测
    waf_info = client.detect_waf("https://target.com")
"""

import asyncio
import logging
import time
from collections import deque
from dataclasses import dataclass, field
from typing import Any, Callable, Dict, List, Optional, Tuple
from urllib.parse import urlparse

import requests
from requests.adapters import HTTPAdapter
from urllib3.util.retry import Retry

logger = logging.getLogger("SecureHttpClient")


@dataclass
class RequestRecord:
    """请求历史记录"""
    method: str
    url: str
    status_code: int
    elapsed_ms: float
    request_headers: Dict[str, str]
    request_body: Optional[str]
    response_headers: Dict[str, str]
    response_body_preview: str  # 前 500 字符
    timestamp: float


@dataclass
class WAFDetection:
    """WAF 检测结果"""
    detected: bool
    vendor: Optional[str]
    confidence: float  # 0.0 - 1.0
    indicators: List[str]
    block_status_codes: List[int]


@dataclass
class ClientConfig:
    """客户端配置"""
    timeout: int = 10
    verify_ssl: bool = False
    proxy: Optional[str] = None
    user_agent: str = "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36"
    rate_limit: float = 0.0              # 每秒最大请求数，0 = 不限速
    retries: int = 2
    retry_backoff: float = 0.5
    retry_status_codes: tuple = (500, 502, 503, 504)
    cookies: Optional[Dict[str, str]] = None
    headers: Optional[Dict[str, str]] = None
    follow_redirects: bool = True
    max_redirects: int = 5
    log_requests: bool = False
    history_size: int = 100              # 请求历史最大条数
    history_body_preview: int = 500      # 响应体预览长度


