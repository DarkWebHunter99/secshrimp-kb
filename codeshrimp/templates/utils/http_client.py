class AsyncSecureHttpClient:
    """
    异步安全 HTTP 客户端
    
    需要: pip install httpx
    """

    def __init__(self, config: Optional[ClientConfig] = None, **kwargs):
        try:
            import httpx
        except ImportError:
            raise ImportError("异步模式需要 httpx: pip install httpx")

        if config is None:
            config = ClientConfig(**kwargs)
        self.config = config
        self._client: Optional[httpx.AsyncClient] = None

    async def __aenter__(self):
        import httpx
        self._client = httpx.AsyncClient(
            timeout=self.config.timeout,
            verify=self.config.verify_ssl,
            proxy=self.config.proxy,
            follow_redirects=self.config.follow_redirects,
            headers={"User-Agent": self.config.user_agent},
            limits=httpx.Limits(max_connections=50, max_keepalive_connections=20),
        )
        return self

    async def __aexit__(self, *args):
        if self._client:
            await self._client.aclose()

    async def aget(self, url: str, **kwargs):
        return await self._client.get(url, **kwargs)

    async def apost(self, url: str, **kwargs):
        return await self._client.post(url, **kwargs)

    async def abatch_get(self, urls: List[str], concurrency: int = 20):
        """异步批量 GET"""
        import asyncio
        semaphore = asyncio.Semaphore(concurrency)

        async def _fetch(url):
            async with semaphore:
                try:
                    resp = await self.aget(url)
                    return (url, resp)
                except Exception as e:
                    return (url, None)

        return await asyncio.gather(*[_fetch(u) for u in urls])


