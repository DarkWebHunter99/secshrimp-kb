def create_burp_client(port: int = 8080) -> SecureHttpClient:
    """创建 Burp Suite 代理客户端"""
    return SecureHttpClient(
        proxy=f"http://127.0.0.1:{port}",
        rate_limit=5.0,
        verify_ssl=False,
        log_requests=True,
    )


def create_scanner_client(rate_limit: float = 10.0, concurrency: int = 5) -> SecureHttpClient:
    """创建扫描器客户端（高并发、限速）"""
    return SecureHttpClient(
        rate_limit=rate_limit,
        retries=1,
        timeout=5,
        log_requests=False,
    )
